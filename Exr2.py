import numpy as np

x = np.array([[1,2],[3,4]])
y = np.array([[9,8,3],[1,6,6]])

print("The element using addition of matrix is: ")
print(np.add(x,y))
print("The element using subtraction of matrix is: ")
print(np.subtract(x,y))
print("The element using division of matrix is: ")
print(np.divide(x,y))